package class14;
//  Write a Java program to Print Pattern 1 10 2 9 3 8 4 7 5 6

public class HomeW14_3 {
    public static void main(String[] args) {
        int i = 1;
        int j = 10;
        while (i <= 5 && j >= 6){
            System.out.println(i);
            i++;
            System.out.println(j);
            j--;
        }
    }
}
